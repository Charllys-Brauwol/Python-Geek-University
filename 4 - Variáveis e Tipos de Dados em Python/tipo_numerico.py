"""
Tipo Float, decimal

OBS: O separador é ponto e não virgula
"""
valor = 1.44
print(valor)
print(type(valor))

#Podemos converter um float para int e de int para float

res = int(valor)
print(res)
print(type(res))

res2 = float(num)
print(res2)
print(type(res2))

#Podemos trabalhar com números complexos

com = 5j

